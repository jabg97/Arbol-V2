    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        try {
            String[] opciones = {"PreOrden", "InOrden", "PostOrden"};
            String opcion = (String) JOptionPane.showInputDialog(this, "Recorrer arbol",
                    "Recorrido", JOptionPane.QUESTION_MESSAGE, null, opciones, opciones[0]);
            if (this.camino != null) {
                this.camino.stop();
                this.camino = null;
                this.lienzo.setnodoCamino(null);
                this.lienzo.setLista("| ");
            }
            this.camino = new Camino(opcion);
            this.camino.start();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

