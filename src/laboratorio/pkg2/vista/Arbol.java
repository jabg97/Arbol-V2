    private class Camino extends Thread {

        private String opcion;
        private final int TIME = 600;

        public Camino(String opcion) {
            this.opcion = opcion;
        }

        @Override
        public void run() {

            try {

                if (this.opcion.equals("PreOrden")) {
                    preorden(lienzo.getArbol().getRaiz());
                } else if (this.opcion.equals("InOrden")) {
                    inorden(lienzo.getArbol().getRaiz());
                } else {
                    postorden(lienzo.getArbol().getRaiz());
                }
                System.out.println("");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        }

        public void inorden(Nodo nodo) {
            if (nodo != null) {
                try {
                    inorden(nodo.getIzquierda());
                    lienzo.setnodoCamino(nodo);
                    lienzo.addLista(nodo.getValor());
                    sleep(TIME);
                    inorden(nodo.getDerecha());
                } catch (Exception ex) {
                }
            }
        }

        public void preorden(Nodo nodo) {
            if (nodo != null) {
                try {
                    lienzo.setnodoCamino(nodo);
                    lienzo.addLista(nodo.getValor());
                    sleep(TIME);
                    preorden(nodo.getIzquierda());
                    preorden(nodo.getDerecha());
                } catch (Exception ex) {
                }
            }
        }

        public void postorden(Nodo nodo) {
            if (nodo != null) {
                try {
                    postorden(nodo.getIzquierda());
                    postorden(nodo.getDerecha());
                    lienzo.setnodoCamino(nodo);
                    lienzo.addLista(nodo.getValor());
                    sleep(TIME);
                } catch (Exception ex) {
                }
            }
        }

    }

